package io.github.lix3nn53.guardiansofadelia.guardian.skill.tree;

public class SkillTreeOffset {
    private final int x;
    private final int y;

    public SkillTreeOffset(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
